<h5 class="job_app_head">
            Mission            
</h5>
<p style="line-height: 1.3;">
    Dans le cadre des Spécifications d'Agrément d'Entretien, du Manuel d'Entretien et du Minimum Equipment List (MEL) de la Compagnie, assurer la gestion, le suivi et la conformité des tâches de la maintenance pour la flotte de BOEING 737 Classique et Nouvelle Génération de la compagnie.
</p><br>
<p><b>     Vous aurez pour missions :</b>
</p>
<ul style="list-style-type:disc; line-height: 1.3;">
    <li> L’animation de l’équipe du lancement, suivi technique et de la saisie des ATL dont la formation, la gestion des absences et le transfert de compétences</li>
        <li> D’assurer des enregistrements relatifs aux :</li>
        <li> Travaux réalisés et travaux sous-traités dans le logiciel</li>
        <li>Mouvements d’équipements d’après ATL, EASA</li>
        <li>Traitements des cartes de travail AD-CN et les travaux reportés et de la mise à jour des livrets aéronef / GTR/ APU</li>
        <li>Dossiers de visites (signatures, présence, CRS, N/R et EASA form1) et ATL/ACL/SPL</li>
        <li>La gestion du planning de l’équipe en charge des suivis de visites sous-traitées chez le prestataire (Suivi de chantier).</li>
        <li>L’élaboration et la planification des tâches du Programme d’Entretien et défauts de ligne à corriger.</li>
        <li>La supervision de l’équipe en charge de la préparation et de la constitution des dossiers de visites via des work-packages : Visites A, C, Inspections AD Pemco, immobilisations spéciales (changement moteur,…), recherche et réservation de sous-traitants pour des travaux particuliers planifiés...</li>
        <li>La mise à jour du programme d’immobilisation et optimisation de celles –ci en liaison avec le responsable du Suivi Flotte.</li>
        <li>D’être le Référent Technique dans le cadre du changement de logiciel. </li>
</ul>
<br>
<p style="line-height: 1.3;"> Ces différentes missions ne sont pas exhaustives et sont susceptibles d’être modifiées en fonction des évolutions et de l’adaptation de l’activité de la Compagnie.</p>
<br><br>
